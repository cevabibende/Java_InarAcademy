package chapters.chapter04.classroom;

public class MathMethods {

	public static void main(String[] args) {
		
		
		System.out.println("ceil denemesi");
		System.out.println(Math.ceil(3.5));
		System.out.println(Math.ceil(3.3));
		System.out.println(Math.ceil(3.6));
		
		
		
		System.out.println("floor denemesi");
		System.out.println(Math.floor(3.5));
		System.out.println(Math.floor(3.3));
		System.out.println(Math.floor(3.6));
		
		System.out.println("rint denemesi");
		System.out.println(Math.rint(3.5));
		System.out.println(Math.rint(3.3));
		System.out.println(Math.rint(3.6));

		
		System.out.println("round denemesi");
		System.out.println(Math.round(3.5));
		System.out.println(Math.round(3.3));
		System.out.println(Math.round(3.6));
		

		System.out.println("max denemesi");
		System.out.println(Math.max(3.5 , 4));
		
		System.out.println("min denemesi");
		System.out.println(Math.min(3.5 , 4));
		
		System.out.println("abs denemesi");
		System.out.println(Math.abs( 4));
		System.out.println(Math.abs(-4));
		
		System.out.println("random denemesi");
		
		
		System.out.println(                  (int)(Math.random() * 1000)         );

		
		System.out.println(  (int)(Math.random() * 3 + 5)  );
		
		
		
		
		
		
		
		
		
	}
	
	
	
}